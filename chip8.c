#include <stdlib.h>

#define RAM_SIZE 4096 // in bytes

/* ================ Font data ================== */

#define SPRITE_SIZE   5 // in bytes
#define   NUM_CHARS   16
#define        ZERO   {0xF0, 0x90, 0x90, 0x90, 0xF0}
#define         ONE   {0x20, 0x60, 0x20, 0x20, 0x70}
#define         TWO   {0xF0, 0x10, 0xF0, 0x80, 0xF0}
#define       THREE   {0xF0, 0x10, 0xF0, 0x10, 0xF0}
#define        FOUR   {0x90, 0x90, 0xF0, 0x10, 0x10}
#define        FIVE   {0xF0, 0x80, 0xF0, 0x10, 0xF0}
#define         SIX   {0xF0, 0x80, 0xF0, 0x90, 0xF0}
#define       SEVEN   {0xF0, 0x10, 0x20, 0x40, 0x40}
#define       EIGHT   {0xF0, 0x90, 0xF0, 0x90, 0xF0}
#define        NINE   {0xF0, 0x90, 0xF0, 0x10, 0xF0}
#define           A   {0xF0, 0x90, 0xF0, 0x90, 0x90}
#define           B   {0xE0, 0x90, 0xE0, 0x90, 0xE0}
#define           C   {0xF0, 0x80, 0x80, 0x80, 0xF0}
#define           D   {0xE0, 0x90, 0x90, 0x90, 0xE0}
#define           E   {0xF0, 0x80, 0xF0, 0x80, 0xF0}
#define           F   {0xF0, 0x80, 0xF0, 0x80, 0x80}

/* ============================================== */

void loadFonts(void* ram) {
    int fonts[NUM_CHARS][SPRITE_SIZE] = {ZERO, ONE, TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE, A, B, C, D, E, F};

    for (int character = 0; character < NUM_CHARS; character++) {
        ram = fonts[character];
        ram += sizeof(fonts[character]);
    }
}

int main() {

    void* ram = malloc(RAM_SIZE);

    loadFonts(ram);

    return 0;
}